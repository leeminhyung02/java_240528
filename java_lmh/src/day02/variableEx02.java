package day02;

public class variableEx02 {

	public static void main(String[] args) {
		int num1 = 0x11;  //16진수-17
		int num2 = 0b11; //2진수-3
		int num3 = 011;   //8진수-9
		int num4 = 11;     //10진수-11
		
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num4);
		

	}

}
